function Fechar() {

    let detal  =  document.getElementById('detal')
    
    detal.style.display = 'none';


}


function Detalhes() {

    let detal  =  document.getElementById('detal')
    
    detal.style.display = 'flex';
    detal.style.flexDirection = 'column';
    detal.style.justifyContent ='center';
    detal.style.alignItems ='center';
    detal.style.gap = '40px'
    

}
