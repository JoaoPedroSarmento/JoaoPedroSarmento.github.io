let n = window.prompt('Digite um número -- > ')
console.log(n) // Sem o parseFloat
// com parseFloat
console.log(parseFloat(n)) // parseFloat tranforma uma string em um número que pode possuir casas decimais 
// parseInt
console.log(parseInt(n)) //  Transforma uma string em um número inteiro 

console.log(n.toString()) // Transforma um número ou qualquer outra  coisa em uma string 